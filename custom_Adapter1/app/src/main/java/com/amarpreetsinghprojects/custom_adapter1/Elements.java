public class Elements {

    String text1;
    String buttonText;
    String text2;

    public Elements(String text1, String buttonText, String text2) {
        this.text1 = text1;
        this.buttonText = buttonText;
        this.text2 = text2;
    }

    public String getText1() {
        return text1;
    }

    public String getButtonText() {
        return buttonText;
    }

    public String getText2() {
        return text2;
    }
}
